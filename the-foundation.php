                <h2>La Maison Corm</h2>
                <p>Tel un phare qui illumine Beyrouth, la tour blanche de Charles Corm fut longtemps l’une des plus hautes structures de la ville. Imaginée  en 1928 par Corm lui-même, la tour est un clin d’œil aux gratte-ciels américains qui se construisent à l’époque. Destinée à héberger l’agence Ford et son administration dont il est le représentant pour le Levant, de nombreuses voitures furent montées ici. L’intérieur garde encore des traces de ce passé industriel avec des phares de voitures employés comme luminaires… En 1934, l’immeuble devient la résidence de la famille Corm. C’est ici que ses quatre enfants grandissent entourés de meubles Art Déco et Bauhaus ainsi que d’une impressionnante bibliothèque.  </p>
