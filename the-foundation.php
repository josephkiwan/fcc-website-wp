                <p>Mais Corm n’a jamais véritablement laissé le monde des arts et des lettres. A l’âge de 40 ans, il quitte les affaires pour s’adonner à l’écriture.  Il en profite aussi pour épouser Samia Baroudy, Miss Liban 1935 avec qui il aura quatre enfants : David, Hiram, Virginie et Madeleine. </p>
